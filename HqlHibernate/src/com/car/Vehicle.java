package com.car;

public class Vehicle {
	
	private int Vid;
	private String Vname;
	private long Vprice;
	public int getVid() {
		return Vid;
	}
	public void setVid(int vid) {
		Vid = vid;
	}
	public String getVname() {
		return Vname;
	}
	public void setVname(String vname) {
		Vname = vname;
	}
	public long getVprice() {
		return Vprice;
	}
	public void setVprice(long vprice) {
		Vprice = vprice;
	}
	
	


}
